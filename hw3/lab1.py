myInPut = "ABCDEF"


def iToF(myInPut):
    stack = []
    outputStack = []
    for inp in myInPut:

        stack. append(inp)
        arr = stack.copy()

    while len(arr) != 0:
        output = arr.pop()
        outputStack.append(output)

    return outputStack


print(iToF(myInPut))
